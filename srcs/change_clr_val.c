#include "fdf.h"

void			change_clr_val(t_pixel *p, int clr, int val)
{
	t_color		c;

	c.c_32 = (*p)[PXL_CLR];
	c.c_8[clr] += val;
	(*p)[PXL_CLR] = c.c_32;
}
